const getTranslation = (language, key) => {
  const value = key.split('.').reduce((result, segment) => result?.[segment], translations[language]);
  return value ?? key;
};

const interpolate = (template, values) =>
  Object.entries(values || {}).reduce(
    (result, [key, value]) => result.replaceAll(`{{${key}}}`, toEnglishDigits(value ?? '')),
    template
  );

const readStoredLanguage = () => {
  try {
    const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return storedLanguage === 'ar' ? 'ar' : 'en';
  } catch {
    return 'en';
  }
};

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => readStoredLanguage());
  const direction = language === 'ar' ? 'rtl' : 'ltr';

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.documentElement.dir = direction;
    document.body.dir = direction;
  }, [direction, language]);

  const value = useMemo(
    () => ({
      language,
      direction,
      setLanguage,
      t: (key, values) => {
        const translation = getTranslation(language, key);
        return typeof translation === 'string'
          ? toEnglishDigits(interpolate(translation, values))
          : translation;
      }
    }),
    [direction, language]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
};
